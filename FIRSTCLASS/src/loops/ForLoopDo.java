package loops;

public class ForLoopDo {

	public static void main(String[] args) {


		for(int row=1; row<=6; row++)
		{
			for(int col=1; col<=3; col++)
			{
				System.out.println("Row number="+row+" Column number ="+col);
			}
			
			System.out.println("***********************************");
		}

	}

}
